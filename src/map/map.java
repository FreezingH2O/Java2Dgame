	    private int tileSize = main.getTilesize();
	    private int mapWidth = 30*48, mapHeight=30*48;

	    private double cameraX, cameraY; // Camera position
	    private double screenWidth  = main.getWidth(), screenHeight=main.getHeight(); // Screen dimensions
        private static Player player;
	        public map(Player player) {
	            this.player = player;
	          
	        }

	        public void updateCamera() {
	            // Center the camera around the player
	            cameraX = player.getPosX() - screenWidth / 2 + tileSize / 2;
	            cameraY = player.getPosY() - screenHeight / 2 + tileSize / 2;

	            // Prevent the camera from moving outside the map boundaries
	            if (cameraX < 0) cameraX = 0;
	            if (cameraY < 0) cameraY = 0;
	            if (cameraX > screenWidth) cameraX = screenWidth;
	            if (cameraY > screenHeight) cameraY = screenHeight;
	        }

	        public void update(GraphicsContext gc) {
	            // Update the camera position
	            updateCamera();
	            player.update(gc);
	            // Draw the map tiles based on the camera position
	            for (int y = 0; y < arr.length; y++) {
	                for (int x = 0; x < arr[y].length; x++) {
	                    double screenX = x * tileSize - cameraX;
	                    double screenY = y * tileSize - cameraY;

	                    if (arr[y][x] == 0) {
	                        gc.drawImage(grass, screenX, screenY);
	                    } else if (arr[y][x] == 1) {
	                        gc.drawImage(brick, screenX, screenY);
	                    }
	                }
	            }

	            // Draw the player at the adjusted position (centered on camera)
	           
	        }

	        public void movePlayer(double dx, double dy) {
	            player.setPosX(player.getPosX() + dx);
	            player.setPosY(player.getPosY() + dy);
	        }
